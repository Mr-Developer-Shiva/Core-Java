public class MathsFunction {
    public static void main(String[] args){
        int a = 2 , b = 3 ; 
        System.out.println(Math.abs(-80));
        System.out.println(Math.floor(3.987));
        System.out.println(Math.ceil(3.482));
        System.out.println(Math.round(6.567));

        System.out.println(Math.min(a,b));
        System.out.println(Math.max(a,b));
        System.out.println(Math.sqrt(a));
        System.out.println(Math.sin(a));
        System.out.println(Math.tan(a));
        System.out.println(Math.cos(a));
        System.out.println(Math.log(a));
        System.out.println(Math.log10(a));
        System.out.println(Math.pow(a,7));

    }
}
